char GetNumCh()
{
	char ch='\0';
	
	if(kbhit())
	{
		if((ch=getch())==0x0)
		{
			getch();
		}
		if(ch==0x8||ch=='D'||ch=='d')
		{
			ch='D';
		}else if(ch=='C'||ch=='c')
		{
			ch='C';
		}else if(ch==0x1b||ch=='E'||ch=='e')
		{
			ch='E';
		}else if(ch=='\x0d'||ch=='=')
		{
			ch='=';
		}else if((ch>='0'&&ch<='9')||(ch=='+'||ch=='-'||ch=='*'||ch=='/'||ch=='^'||ch=='.'||ch=='('||ch==')'))
		{
			ch=ch;
		}
		
	}
	
	
	return ch;
}
