char GetNumCh()
{
	char ch;
	if(!kbhit())
	{
		if((ch=getch())==0x0)
		{
			ch=getch();
				
			
		}else if((ch>='0'&&ch<='9')||(ch=='+'||ch=='-'||ch=='*'||ch=='/'||ch=='^'||ch=='='||ch=='.')||ch==0x1)
		{
			return ch;
		}else if(ch==0x8)
		{
			return 'D';
		}
		
	}
	return '\0';
}
